package org.eclipse.cq.ss.beans;

public class Function {

	String name;
	String ncss;
	String ccn;
	String javadocs;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNcss() {
		return ncss;
	}

	public void setNcss(String ncss) {
		this.ncss = ncss;
	}

	public String getCcn() {
		return ccn;
	}

	public void setCcn(String ccn) {
		this.ccn = ccn;
	}

	public String getJavadocs() {
		return javadocs;
	}

	public void setJavadocs(String javadocs) {
		this.javadocs = javadocs;
	}

}
